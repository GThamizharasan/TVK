
import React from 'react';

interface NewsTickerProps {
  items: string[];
}

const NewsTicker: React.FC<NewsTickerProps> = ({ items }) => {
  const displayItems = items.length > 0 ? items : ["Welcome to the Official TVK Portal. Stay tuned for live updates."];

  const shareNews = (platform: 'x' | 'fb' | 'wa', text: string) => {
    const url = window.location.href;
    const shareText = `TVK Update: ${text}`;
    let shareUrl = '';

    switch (platform) {
      case 'x':
        shareUrl = `https://twitter.com/intent/tweet?text=${encodeURIComponent(shareText)}&url=${encodeURIComponent(url)}`;
        break;
      case 'fb':
        shareUrl = `https://www.facebook.com/sharer/sharer.php?u=${encodeURIComponent(url)}&quote=${encodeURIComponent(shareText)}`;
        break;
      case 'wa':
        shareUrl = `https://api.whatsapp.com/send?text=${encodeURIComponent(shareText + ' ' + url)}`;
        break;
    }

    window.open(shareUrl, '_blank', 'noopener,noreferrer');
  };

  return (
    <div className="bg-red-700 text-white py-2 overflow-hidden relative border-y border-red-800 select-none group/ticker">
      <div className="flex whitespace-nowrap news-ticker-animation">
        {displayItems.concat(displayItems).map((item, index) => (
          <div key={index} className="mx-12 flex items-center gap-6">
            <span className="font-bold uppercase tracking-wider flex items-center">
              <span className="w-2.5 h-2.5 bg-yellow-400 rounded-full mr-3 shadow-[0_0_8px_rgba(250,204,21,0.6)]"></span>
              {item}
            </span>
            
            {/* Share Controls */}
            <div className="flex items-center gap-2 px-3 py-1 bg-white/10 rounded-full backdrop-blur-sm border border-white/5 opacity-0 group-hover/ticker:opacity-100 transition-opacity duration-300">
              <span className="text-[9px] font-black mr-1 text-red-200">SHARE</span>
              
              {/* X Icon */}
              <button 
                onClick={() => shareNews('x', item)}
                className="p-1 hover:text-black hover:bg-white rounded transition-all"
                title="Share on X"
              >
                <svg className="w-3.5 h-3.5" fill="currentColor" viewBox="0 0 24 24">
                  <path d="M18.244 2.25h3.308l-7.227 8.26 8.502 11.24H16.17l-5.214-6.817L4.99 21.75H1.68l7.73-8.835L1.254 2.25H8.08l4.713 6.231zm-1.161 17.52h1.833L7.084 4.126H5.117z"/>
                </svg>
              </button>

              {/* FB Icon */}
              <button 
                onClick={() => shareNews('fb', item)}
                className="p-1 hover:text-blue-600 hover:bg-white rounded transition-all"
                title="Share on Facebook"
              >
                <svg className="w-3.5 h-3.5" fill="currentColor" viewBox="0 0 24 24">
                  <path d="M24 12.073c0-6.627-5.373-12-12-12s-12 5.373-12 12c0 5.99 4.388 10.954 10.125 11.854v-8.385H7.078v-3.47h3.047V9.43c0-3.007 1.792-4.669 4.533-4.669 1.312 0 2.686.235 2.686.235v2.953H15.83c-1.491 0-1.956.925-1.956 1.874v2.25h3.328l-.532 3.47h-2.796v8.385C19.612 23.027 24 18.062 24 12.073z"/>
                </svg>
              </button>

              {/* WA Icon */}
              <button 
                onClick={() => shareNews('wa', item)}
                className="p-1 hover:text-green-500 hover:bg-white rounded transition-all"
                title="Share on WhatsApp"
              >
                <svg className="w-3.5 h-3.5" fill="currentColor" viewBox="0 0 24 24">
                  <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413Z"/>
                </svg>
              </button>
            </div>
          </div>
        ))}
      </div>
    </div>
  );
};

export default NewsTicker;
